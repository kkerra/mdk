def bit_and(a, b):
    return f"{a} & {b} = {a & b}"

def bit_or(a, b):
    return f"{a} | {b} = {a | b}"

def bit_xor(a, b):
    return f"{a} ^ {b} = {a ^ b}"

def bit_not(a):
    return f"~{a} = {~a}"

def bit_left_shift(a, b):
    return f"{a} << {b} = {a << b}"

def bit_right_shift(a, b):
    return f"{a} >> {b} = {a >> b}"